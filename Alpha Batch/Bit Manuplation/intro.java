import java.util.*;

public class intro {

    public static void main(String[] args) {
        System.out.println(5^6); //xor -> different bits = 1 otherwise 0
        System.out.println(5&6);//and
        System.out.println(5|6);//or
        System.out.println(~5); //not

/*          Binary Left Shift: a<<b = (a)*(2^b)              */
System.out.println(5<<2);
/*          Binary Right Shift:a>>b = (a)/(2^b)              */ 


}
}